public class CfBlob {

    private long id;
    private int idCfBlob;
    private String configuracao;
    private String principal;
    private String secundario;
    private String terciario;
    private String quaternario;
    private String mobile;
    private String nome;
    private String status;
    private String testado;
    private String data;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public int getIdCfBlob() {
        return idCfBlob;
    }

    public void setIdCfBlob(int idCfBlob) {
        this.idCfBlob = idCfBlob;
    }

    public String getConfiguracao() {
        return configuracao;
    }

    public void setConfiguracao(String configuracao) {
        this.configuracao = configuracao;
    }

    public String getPrincipal() {
        return principal;
    }

    public void setPrincipal(String principal) {
        this.principal = principal;
    }

    public String getSecundario() {
        return secundario;
    }

    public void setSecundario(String secundario) {
        this.secundario = secundario;
    }

    public String getTerciario() {
        return terciario;
    }

    public void setTerciario(String terciario) {
        this.terciario = terciario;
    }

    public String getQuaternario() {
        return quaternario;
    }

    public void setQuaternario(String quaternario) {
        this.quaternario = quaternario;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTestado() {
        return testado;
    }

    public void setTestado(String testado) {
        this.testado = testado;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

}
